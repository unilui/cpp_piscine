#include "Zombie.hpp"

Zombie* zombieHorde( int N, std::string name )
{
	Zombie	*horde = new Zombie[N];

	for (int zombie = 0; zombie < N; zombie++)
		horde[zombie].set_name(name);
	return (horde);
}
